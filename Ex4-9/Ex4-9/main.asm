.INCLUDE "m128def.inc"

	LDI R16, 0xff
	OUT DDRB, R16
	OUT PORTB, R16

	CBI PORTB, 0
	CALL DELAY_1S

	CBI PORTB, 1
	CALL DELAY_1S

	CBI PORTB, 2
	CALL DELAY_1S

	CBI PORTB, 3
	CALL DELAY_1S

	CBI PORTB, 4
	CALL DELAY_1S

	CBI PORTB, 5
	CALL DELAY_1S
	
	CBI PORTB, 6
	CALL DELAY_1S

	CBI PORTB, 7
	CALL DELAY_1S

	
DELAY_1S:
	LDI R20, 32
L1:
	LDI R21, 200
L2:
	LDI R22, 250
L3:
	NOP
	NOP
	DEC R22
	BRNE L3
	DEC R21
	BRNE L2
	DEC R20
	BRNE L1
	RET



